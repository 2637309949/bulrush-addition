package template

import (
	"log"
	"os"
	"path"
)

var api_data_js = []byte("\x64\x65\x66\x69\x6e\x65\x28\x7b\x20\x22\x61\x70\x69\x22\x3a\x20\x5b\x5d\x20\x7d\x29\x3b\x0a")

func init() {
	filepath := "/api_data.js"
	if _, err := FS.Stat(CTX, path.Dir(filepath)); os.IsNotExist(err) {
		err = FS.Mkdir(CTX, path.Dir(filepath), 0777)
		if err != nil {
			log.Fatal(err)
		}
	}
	f, err := FS.OpenFile(CTX, filepath, os.O_RDWR|os.O_CREATE|os.O_TRUNC, 0777)
	if err != nil {
		log.Fatal(err)
	}
	_, err = f.Write(api_data_js)
	if err != nil {
		log.Fatal(err)
	}
	err = f.Close()
	if err != nil {
		log.Fatal(err)
	}
}
